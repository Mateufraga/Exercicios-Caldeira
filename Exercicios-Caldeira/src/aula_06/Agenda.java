package aula_06;

public class Agenda {
    String NumeroTelefone;
    String Nome;
    String Endereco;
    String Localidade;

    public Agenda(String numeroTelefone, String nome, String endereco, String localidade) {

        this.NumeroTelefone = numeroTelefone;
        this.Nome = nome;
        this.Endereco = endereco;
        this.Localidade = localidade;

    }
}
