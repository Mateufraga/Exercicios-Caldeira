package aula_08;

public class Calculadora {
        public int adicao(int n1, int n2){
            return n1+n2;
        }

        public int subtracao(int n1, int n2){
            return n1-n2;
        }

        public int multiplicacao(int n1, int n2){
            return n1*n2;
        }

        public int divisao(int n1, int n2){
            return n1/n2;
        }

        public double raizQuadrada(double n1){
            return Math.sqrt(n1);
        }

        public double potenciacao(double n1, double n2){
            return Math.pow(n1,n2);
        }
    }

