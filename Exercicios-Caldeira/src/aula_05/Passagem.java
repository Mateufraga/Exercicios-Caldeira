package aula_05;

public class Passagem {
    String Nome;
    String Destino;
    String CPF;
    String DataEmbarque;
    String DataRetorno;

    public Passagem(String nome, String destino, String cpf, String dataembarque, String dataretorno) {
        this.Nome = nome;
        this.Destino = destino;
        this.CPF = cpf;
        this.DataEmbarque = dataembarque;
        this.DataRetorno = dataretorno;
    }
}
